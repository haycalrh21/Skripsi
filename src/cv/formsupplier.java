 public void kodesupplier(){
        try {
            Connection con = new koneksi().koneksiDatabase();
            java.sql.Statement stat = con.createStatement();
                stat=con.createStatement();
                String sql="select * from supplier order by idsupplier desc";

            ResultSet rs=stat.executeQuery(sql);
            if (rs.next()) {

                String nofak = rs.getString("idsupplier").substring(2);

                String AN = "" + (Integer.parseInt(nofak) + 1);

                String Nol = "";
                
                if(AN.length()==1)
                {Nol = "00";}
                else if(AN.length()==2)
                {Nol = "0";}
                else if(AN.length()==3)
                {Nol = "";}
               tfidsupplier.setText("SP" + Nol + AN);
            } else {
                tfidsupplier.setText("SP001");
            }}catch(Exception e){

           JOptionPane.showMessageDialog(null, e);
           }
    }
 
 private void simpan(){
    String query;
        query = "INSERT INTO supplier(idsupplier,namasupplier,namabarang,jenisbarang,jumlahbarang,namapic)"
                + "VALUES (?,?,?,?,?,?)";
        PreparedStatement statement;
        Connection connection;
        try {
        connection = koneksi.koneksiDatabase();
        statement = connection.prepareStatement(query);
        statement.setString(1, tfidsupplier.getText());
        statement.setString(2, tfnamasupplier.getText());
        statement.setString(3, tfnamabarang.getText());
        statement.setString(4, cbjenisbarang.getSelectedItem().toString());
        statement.setString(5, tfjumlahbarang.getText());
        statement.setString(6, tfpic.getText());
        
        int hasil = statement.executeUpdate();
        if(hasil==1){
            JOptionPane.showMessageDialog(this,"DATA SUPPLIER TERSIMPAN");
        }
            }catch (SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
            }

    }
